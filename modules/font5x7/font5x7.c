display_region_t font5x7_char_get_bounds(const font_t *font, wchar_t c, uint16_t scale)
{
  (void)font;
  (void)c;
  display_region_t bounds;
  bounds.x1 = 0;
  bounds.y1 = 0;
  bounds.x2 = indirect_call(udiv)(5*scale, (72*8)).quot - 1;
  bounds.y2 = indirect_call(udiv)(7*scale, (72*8)).quot - 1;
  return bounds;
}

bool font5x7_get_char_pixel(const font_t *font, wchar_t c, display_coord_t coord, uint16_t scale)
{
  (void)font;
  (void)scale;
  if (c < ' ' || c > '~')
  {
    return false;
  }
  uint32_t bitmap_char = (uintptr_t)&font5x7.data[(c - ' ') * 5] + GET_MODULE_DATA_PTR_OFFSET();
  uint8_t x = indirect_call(udiv)(coord.x * (72*8), scale).quot;
  uint8_t y = indirect_call(udiv)(coord.y * (72*8), scale).quot;
  if (pgm_read_byte_far(bitmap_char+x) & (0x40 >> y))
  {
    return true;
  }
  return false;
}

void font5x7_get_char_pixels(const font_t *font, wchar_t c, display_region_t box, uint8_t *out, uint16_t scale)
{
  for (uint8_t y = box.y1; y <= box.y2; y++)
  {
    for (uint8_t x = box.x1; x <= box.x2; x++)
    {
      if (font5x7_get_char_pixel(font, c, (display_coord_t){x, y}, scale))
      {
        out[x - box.x1 + (y - box.y1) * (box.x2 - box.x1 + 1)] = 1;
      }
      else
      {
        out[x - box.x1 + (y - box.y1) * (box.x2 - box.x1 + 1)] = 0;
      }
    }
  }
}

display_coord_t font5x7_get_char_advance(const font_t *font, wchar_t c, uint16_t scale)
{
  (void)font;
  (void)c;
  display_coord_t advance;
  advance.x = indirect_call(udiv)(6*scale, (72*8)).quot;
  advance.y = 0;
  return advance;
}

